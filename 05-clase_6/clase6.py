# operadores numericos

a = 10
b = 3

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)

# modulo (%)
print("Modulo:", a % b)

# parte entera de la division (//)
print("Parte entera división:", a // b)

# potencioación (**)
print("Potenciación:", a ** b)

